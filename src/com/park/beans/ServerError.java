package com.park.beans;

public class ServerError {
	private int error;
	private String describe;
	public int getError() {
		return error;
	}
	public void setError(int error) {
		this.error = error;
	}
	public String getDescribe() {
		return describe;
	}
	public void setDescribe(String describe) {
		this.describe = describe;
	}
}
